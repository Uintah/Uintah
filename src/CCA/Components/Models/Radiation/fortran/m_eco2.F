      SUBROUTINE m_ECO2(OMEGA,TEMP,GC,SD15,SDWEAK,GDINV,GDDINV)

      IMPLICIT NONE

      DOUBLE PRECISION AA,BB,CC,DD,EE,FF,GG,SMINUS,SPLUS,SDWEAK,
     &SDSTRG,DINV,GD,GDINV,GDDINV,OMEGA,WM,TEMP,OM1,OM2,OM3,TO,C2,
     &BE,COM1,COM2,COM3,A,B,X13,X23,X33,T0,XBAR,OM12,ALPHA,OMPRIM,
     &V,G,VBAR1,V3,GAM,OMVV3,DELTA,D,OMVBAR,F1,F2,UNFLO1,UNFLO2,
     &UNFLO3,TEST,OMA,OMB,TTEMP,W1,WW,T1,TT,TW,DINV1,DINV2,DINV3,
     &TEMP1,TEMP2,TEMP3

      DOUBLE PRECISION ATOT(3),BCNT(3),GC(5),SD15(480)

      INTEGER I,J,K,L

      IF(OMEGA.GT.5725.)GO TO 300

      WM=44.
      GD=5.94E-6*OMEGA*(TEMP/(273.*WM))**.5

      IF(OMEGA.GT.4550.)GO TO 500
      IF(OMEGA.GT.3800.)GO TO 300
      IF(OMEGA.GT.3050.)GO TO 100
      IF(OMEGA.GT.2474.)GO TO 300
      IF(OMEGA.GT.1975.)GO TO 100
      IF(OMEGA.GT.1100.)GO TO 300
      IF(OMEGA.GT.880.)GO TO 600
      IF(OMEGA.GT.500.)GO TO 400
      GO TO 300

CONTRIBUTION TO 2.0 MICRON BAND FROM (000)-(041),(000)-(121),AND (000)
C     -(201) TRANS.

500   OM1=1354.91
      OM2=673.0
      OM3=2396.49
      BCNT(1)=4860.5
      BCNT(2)=4983.5
      BCNT(3)=5109.0
      TO=300.
      C2=1.4388
      BE=0.391635
      COM1=4.*OM2+OM3
      COM2=OM1+2.*OM2+OM3
      COM3=2.*OM1+OM3
      ATOT(3)=0.426*TO/TEMP*(1.-EXP(-C2*COM3/TEMP/(1.-EXP(-C2*OM1/TEMP))
     1))**2/(1.-EXP(-C2*OM3/TEMP))
      ATOT(2)=1.01*TO/TEMP*(1.-EXP(-C2*COM2/TEMP))/(1.-EXP(-C2*OM1/TEMP)
     1)/(1.-EXP(-C2*OM2/TEMP))**2/(1.-EXP(-C2*OM3/TEMP))
      ATOT(1)=0.272*TO/TEMP*(1.-EXP(-C2*COM1/TEMP))/(1.-EXP(-C2*OM2/TEMP
     1))**4/(1.-EXP(-C2*OM3/TEMP))
      SDWEAK=0.0

      DO 510 K=1,3
      SDWEAK=SDWEAK+ATOT(K)*C2/(4.*BE*TEMP)*ABS(OMEGA-BCNT(K))
     1*EXP(-C2/(4.*BE*TEMP)*(OMEGA-BCNT(K))**2)
510   CONTINUE

      DINV=1./(4.*BE)
      GDINV=GC(1)*DINV
      GDDINV=GD*DINV
C***EXPRESS S/D AT STP, AS IS IN NASA SP-3080
      SDWEAK=SDWEAK*TEMP/273.
      RETURN

100   CONTINUE
      B=.391635
      A=.0030875
      X13=-19.37
      X23=-12.53
      X33=-12.63
      OM1=1354.91
      OM2=673.
      OM3=2396.49
      T0=300.
      C2=1.4388
      XBAR=.5*(.5*X13+X23)
      OM12=.5*(.5*OM1+OM2)
      SDWEAK=0.
      SDSTRG=0.
      IF(OMEGA.LE.2395.)GO TO 200
CALCULATE ABSORPTION COEF. AND LINE SPACING PARAMETER FOR 2.7 MICRON BAND
      L=1
CONTRIBUTION TO 2.7 MICRON BAND FROM (000)-(021) AND (010)-(031) TRANS.
      ALPHA=28.5
      OMPRIM=2.*OM2+OM3
120   AA=ALPHA*B*C2/(A*(1.-EXP(-OM3*C2/T0))*(1.-EXP(-OM12*C2/T0))**3
     1*(1.+EXP(-OM12*C2/T0))*(1.-EXP(-OMPRIM*C2/T0)))
      BB=(1.-EXP(-C2*OMEGA/TEMP))*(1.-EXP(-C2*OM3/TEMP))*
     1(1.-EXP(-OM12*C2/TEMP))**3*(1.+EXP(-OM12*C2/TEMP))
     2 *(1.-EXP(-C2*OMPRIM/TEMP))
      CC=AA*BB*OMEGA/TEMP*T0/TEMP

      DO 102 J=1,20

      V=FLOAT(J-1)
      IF(J/2*2.EQ.J)G=(V+1.)*(V+3.)/4.
      IF(J/2*2.NE.J)G=(V+2.)*(V+2.)/4.
      VBAR1=-1.+(V+3.)*(V+4.)/(V+2.)/6.
      IF(J/2*2.EQ.J)VBAR1=-1.+(V+5.)/6.

      DO 101 K=1,10

      V3=FLOAT(K-1)
      DD=(V3+1)*G*EXP(-(V3*OM3+V*OM12)*C2/TEMP)*(VBAR1+1.)
      GAM=B-A*(V3+1.)
      IF(L.EQ.2)GO TO 125
      OMVV3=3598.-18.*V-47.*V3
      IF(V.EQ.0.)OMVV3=3613.-47.*V3
      GO TO 130
125   OMVV3=3728.-5.*V-47.*V3
      IF(V.EQ.0.)OMVV3=3715.-47.*V3
130   DELTA=A*(OMEGA-OMVV3)
      IF(GAM*GAM.LE.DELTA)GO TO 102
      D=2.*(GAM*GAM-DELTA)**.5
      OMVBAR=OMVV3*(1.-EXP(-OMVV3*C2/TEMP))
      F1=GAM-D/2
      F2=GAM+D/2.
      EE=C2*GAM/(A*A*TEMP)
      UNFLO1=EE*DELTA*(1.+.5*A/GAM)
      IF(UNFLO1.LE.-78.)GO TO 102
      UNFLO2=EE*2.*GAM*F1
      IF(UNFLO2.GE.78.)GO TO 102
      FF=DEXP(EE*DELTA*(1.+.5*A/GAM))
      SMINUS=CC*DD/OMVBAR*ABS(F1)*FF*DEXP(-EE*2.*GAM*F1)
      UNFLO3=EE*2.*GAM*F2
      IF(UNFLO3.GE.78.)GO TO 160
      SPLUS=CC*DD/OMVBAR*ABS(F2)*FF*DEXP(-EE*2.*GAM*F2)
      GO TO 170
160   SPLUS=0.
170   GG=SDWEAK
      SDWEAK=(SMINUS+SPLUS)/D+SDWEAK
      TEST=(SDWEAK-GG)/SDWEAK
      IF(TEST.LT..0001)GO TO 102
      SDSTRG=(.5*G)**.5*(SMINUS**.5+SPLUS**.5)/D+SDSTRG

101   CONTINUE

102   CONTINUE

      IF(L.EQ.2)GO TO 250
CONTRIBUTION TO 2.7 MICRON BAND FROM (000)-(101) AND (010)-(111) TRANS.
      ALPHA=42.3
      OMPRIM=OM1+OM3
      L=2
      GO TO 120

CALCULATE ABSORPTION COEF AND LINE SPACING PARAMETER FOR 4.3 MICRON BAND
200   ALPHA=2700.
      OMPRIM=OM3
      AA=ALPHA*B*C2/(A*(1.-EXP(-OM3*C2/T0))*(1.-EXP(-OM12*C2/T0))**3
     1*(1.+EXP(-OM12*C2/T0))*(1.-EXP(-OMPRIM*C2/T0)))
      BB=(1.-EXP(-C2*OMEGA/TEMP))*(1.-EXP(-C2*OM3/TEMP))*
     1(1.-EXP(-OM12*C2/TEMP))**3*(1.+EXP(-OM12*C2/TEMP))
     2 *(1.-EXP(-C2*OMPRIM/TEMP))
      CC=AA*BB*OMEGA/TEMP*T0/TEMP

      DO 202 J=1,20

      V=FLOAT(J-1)
      IF(J/2*2.EQ.J)G=(V+1.)*(V+3.)/4.
      IF(J/2*2.NE.J)G=(V+2.)*(V+2.)/4.

      DO 201 K=1,10

      V3=FLOAT(K-1)
      DD=(V3+1.)*G*EXP(-(V3*OM3+V*OM12)*C2/TEMP)
      GAM=B-A*(V3+1.)
      OMVV3=OM3+.5*X13+X23+2.*X33+XBAR*V+2.*X33*V3
      DELTA=A*(OMEGA-OMVV3)
      IF(GAM*GAM.LE.DELTA)GO TO 202
      D=2.*(GAM*GAM-DELTA)**.5
      OMVBAR=OMVV3*(1.-EXP(-OMVV3*C2/TEMP))
      F1=GAM-D/2
      F2=GAM+D/2.
      EE=C2*GAM/(A*A*TEMP)
      UNFLO1=EE*DELTA*(1.+.5*A/GAM)
      IF(UNFLO1.LE.-78.)GO TO 202
      UNFLO2=EE*2.*GAM*F1
      IF(UNFLO2.GE.78.)GO TO 202
      FF=DEXP(EE*DELTA*(1.+.5*A/GAM))
      SMINUS=CC*DD/OMVBAR*ABS(F1)*FF*DEXP(-EE*2.*GAM*F1)
      UNFLO3=EE*2.*GAM*F2
      IF(UNFLO3.GE.78.)GO TO 246
      SPLUS=CC*DD/OMVBAR*ABS(F2)*FF*DEXP(-EE*2.*GAM*F2)
      GO TO 247
246   SPLUS=0.
247   GG=SDWEAK
      SDWEAK=(SMINUS+SPLUS)/D+SDWEAK
      TEST=(SDWEAK-GG)/SDWEAK
      IF(TEST.LT..0001)GO TO 202
      SDSTRG=(.5*G)**.5*(SMINUS**.5+SPLUS**.5)/D+SDSTRG

201   CONTINUE

202   CONTINUE

250   CONTINUE
      IF(SDWEAK.EQ.0.)GO TO 300
      DINV=SDSTRG*SDSTRG/SDWEAK
      GDINV=GC(1)*DINV
      GDDINV=GD*DINV
C***EXPRESS S/D AT STP, AS IS K IN NASA SP-3080
      SDWEAK=SDWEAK*TEMP/273.
      RETURN

CONTRIBUTION TO 10.0 MICRON BAND FROM (100)-(001) AND (020)-(001) TRANS.
600   OM1=1354.91
      OM2=673.
      OM3=2396.49
      C2=1.4388
      BCNT(1)=960.8
      BCNT(2)=1063.6
      OMA=OM3
      OMB=(OM1+2.*OM2)/2.
      TO=300.
      ATOT(1)=0.0219
      ATOT(2)=0.0532
      BE=0.391635

      DO 610 K=1,2
      ATOT(K)=TO/TEMP*ATOT(K)*EXP(C2*OMB*(1./TO-1./TEMP))
     1*(1.-EXP(-C2*(OMA-OMB)/TEMP))/(1.-EXP(-C2*OMA/TEMP))
     2/(1.-EXP(-OMB*C2/TEMP))
610   CONTINUE

      SDWEAK=0.

      DO 620 I=1,2
      SDWEAK=SDWEAK+ATOT(I)*C2/(4.*BE*TEMP)*ABS(OMEGA-BCNT(I))
     1*EXP(-C2/(4.*BE*TEMP)*(OMEGA-BCNT(I))**2)
620   CONTINUE

      DINV=1./4./BE
      GDINV=GC(1)*DINV
      GDDINV=GD*DINV
C***EXPRESS S/D AT STP, AS IS IN NASA SP-3080
      SDWEAK=SDWEAK*TEMP/273.

      RETURN

CONTRIBUTION TO 15.0 MICRON BAND FROM (000)-(010) TRANS.
400   TTEMP=TEMP
      J=(OMEGA-495.)/5.
      W1=495.+5.*FLOAT(J)
      WW=(OMEGA-W1)/5
      IF(TEMP.GT.2400.)TEMP=2399.99
      IF(TEMP.LT.300.)TEMP=300.
      I=TEMP/300.
      IF((I.GT.2).AND.(TEMP.LT.1200.))GO TO 410
      IF((I.GT.5).AND.(TEMP.LT.2400.))GO TO 420
      T1=FLOAT(I)*300.
      TT=(TEMP-T1)/300.
      IF(I.GT.4)I=I-1
      GO TO 430
410   I=2
      TT=(TEMP-600.)/600.
      GO TO 430
420   I=5
      TT=(TEMP-1800.)/600.
430   TW=TT*WW
      SDWEAK=SD15(6*(J-1)+I)*(1.-TT-WW+TW)
     & +SD15(6*(J-1)+(I+1))*(TT-TW)
     & +SD15(6*(J+1-1)+I)*(WW-TW)
     & +SD15(6*(J+1-1)+(I+1))*TW
      IF(SDWEAK.EQ.0.)GO TO 300
CALCULATE LINE SPACING PARAMETER FOR 15.0 MICRON BAND
      DINV1=1.2
      DINV2=8.0
      DINV3=30.0
      TEMP1=300.0
      TEMP2=550.0
      TEMP3=830.0
      DINV=DINV1*(TEMP-TEMP2)*(TEMP-TEMP3)/(TEMP1-TEMP2)/
     1(TEMP1-TEMP3)+DINV2*(TEMP-TEMP1)*(TEMP-TEMP3)/(TEMP2-TEMP1)
     2/(TEMP2-TEMP3)+DINV3*(TEMP-TEMP1)*(TEMP-TEMP2)/(TEMP3-TEMP1)
     3/(TEMP3-TEMP2)
      GDINV=GC(1)*DINV
      GDDINV=GD*DINV
      RETURN
300   SDWEAK=0.
      GDINV=1.
      GDDINV=1.

      RETURN
      END







